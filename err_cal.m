function [trans_error,rot_error]=err_cal(T)
t1=[0;0;-100];
t2=[-100;0;0];
t3=[-100;0;-100];
t4=[-100;0;-200];angle1=[0;0;0];
angle2=[0;0;0];
angle3=[0;0;0];
angle4=[0;0;0];
R1=rpy2r(angle1(1),angle1(2),angle1(3),'degree');
R2=rpy2r(angle2(1),angle2(2),angle2(3),'degree');
R3=rpy2r(angle3(1),angle3(2),angle3(3),'degree');
R4=rpy2r(angle4(1),angle4(2),angle4(3),'degree');

T1_noise=T(1);
T2_noise=T(2);
T3_noise=T(3);
T4_noise=T(4);
R1_result=T1_noise.SO3.R;
R2_result=T2_noise.SO3.R;
R3_result=T3_noise.SO3.R;
R4_result=T4_noise.SO3.R;
t1_result=T1_noise.t;
t2_result=T2_noise.t;
t3_result=T3_noise.t;
t4_result=T4_noise.t;
trans_error1=norm(t1-t1_result);
trans_error2=norm(t2-t2_result);
trans_error3=norm(t3-t3_result);
trans_error4=norm(t4-t4_result);
rot_error1=acos(double((trace(R1*R1_result')-1))/2);
rot_error2=acos(double((trace(R2*R2_result')-1))/2);
rot_error3=acos(double((trace(R3*R3_result')-1))/2);
rot_error4=acos(double((trace(R4*R4_result')-1))/2);
trans_error=[trans_error1,trans_error2,trans_error3,trans_error4];
rot_error=[rot_error1,rot_error2,rot_error3,rot_error4];
end